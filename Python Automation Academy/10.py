'''

Arithmetic operations Using assignment operators

'''

number_1 = 30
number_2 = 4

number_1 += number_2
print(number_1)
number_1 -= number_2
print(number_1)
number_1 *= number_2
print(number_1)
number_1 /= number_2
print(number_1)
number_1 %= number_2
print(number_1)
number_1 **= number_2
print(number_1)
number_1 //= number_2
print(number_1)
